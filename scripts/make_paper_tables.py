def falsifier_word(status):
    if status is None:
        return "---"
    return r"\textbf{triggered}" if status else "silent"


def predictions_summary():
    groups, hr = prediction_rows()
    lines = [
        r"% AUTO-GENERATED by scripts/make_paper_tables.py from frozen",
        r"% evaluator artifacts in runs/ -- do not edit by hand.",
        r"\begin{tabular}{@{}l ccc l@{}}",
        r"\toprule",
        r"prediction group & \predpass & \predfail & \predunsc"
        r" & falsifier \\",
        r"\midrule",
    ]
    tot = {PASS: 0, FAIL: 0, UNSC: 0}
    for gname, fals, rows in groups:
        n = {PASS: 0, FAIL: 0, UNSC: 0}
        for _, cls, _ in rows:
            n[cls] += 1
            tot[cls] += 1
        lines.append(f"{gname} & {n[PASS]} & {n[FAIL]} & {n[UNSC]} & "
                     f"{falsifier_word(fals)} \\\\")
    n_trig = sum(1 for _, fals, _ in groups if fals is True)
    n_reg = sum(1 for _, fals, _ in groups if fals is not None)
    lines += [
        r"\midrule",
        f"all & {tot[PASS]} & {tot[FAIL]} & {tot[UNSC]} & "
        f"{n_trig} of {n_reg} \\\\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines)


def predictions_ledger():
    groups, hr = prediction_rows()
    lines = [
        r"% AUTO-GENERATED by scripts/make_paper_tables.py from frozen",
        r"% evaluator artifacts in runs/ -- do not edit by hand.",
        r"\begin{tabular}{@{}l c l@{}}",
        r"\toprule",
        r"registered prediction & verdict & note \\",
        r"\midrule",
    ]
    for gname, fals, rows in groups:
        fnote = "" if fals is None else \
            f" (falsifier {'triggered' if fals else 'silent'})"
        lines.append(r"\multicolumn{3}{@{}l}{\emph{%s%s}} \\"
                     % (gname, fnote))
        for label, cls, note in rows:
            lines.append(f"\\quad {label} & {MARK[cls]} & {note} \\\\")
    lines += [
        r"\midrule",
        r"Blind directional hit rate (Step 6) & "
        f"\\multicolumn{{2}}{{l}}{{{hr['hits']}/{hr['scored']} scored "
        f"({hr['registered']} registered, "
        f"{len(hr['unscoreable'])} unscoreable)}} \\\\",
        r"\bottomrule",
        r"\end{tabular}",
    ]
    return "\n".join(lines)


def main():
    OUT.mkdir(parents=True, exist_ok=True)
    for name, content in [("phase_rvp31.tex", phase_table()),
                          ("step6_doses.tex", step6_table()),
                          ("predictions_summary.tex", predictions_summary()),
                          ("predictions.tex", predictions_ledger())]:
        (OUT / name).write_text(content + "\n")
        print(f"wrote paper/tables/{name}")


if __name__ == "__main__":
    main()
