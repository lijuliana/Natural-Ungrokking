"""Generate paper/tables/*.tex from frozen evaluator artifacts.

Every number in the emitted tables comes from a frozen-evaluator JSON in
runs/ (mirrored on S3). Verdict WORDS that the evaluators printed to the
research log but did not serialize (M4b PASS, M4a/c INSTRUMENT-INVALID,
the pre-tag dn15 FALSIFIED) are transcribed here as documented constants
with their logged source; no numeric value is hand-entered anywhere.

  python scripts/make_paper_tables.py
"""

import json
from pathlib import Path

RUNS = Path("runs")
OUT = Path("paper/tables")

PHASE_FAMILIES = ["pronoun_gender_ref", "reflexive_gender", "det_an_choice",
                  "a_an_adjective", "irregular_past", "negation_bare_verb"]
# column labels are the unique-data/params (D/N) value of the cell,
# grouped under a corpus header in phase_table()
PHASE_CELLS = [("packed", "packed"), ("dn5", "5"), ("dn15", "1.5"),
               ("web", "packed"), ("webdn5", "5"), ("webdn15", "1.5")]
FAM_TEX = {
    "pronoun_gender_ref": r"pronoun\_gender (focal)",
    "reflexive_gender": r"reflexive\_gender",
    "det_an_choice": r"det\_an\_choice",
    "a_an_adjective": r"a\_an\_adjective",
    "irregular_past": r"irregular\_past",
    "negation_bare_verb": r"negation\_bare\_verb",
}


def j(name):
    return json.loads((RUNS / name).read_text())


def phase_letters(s):
    # uppercase = classifiable verdict; lowercase in the frozen table =
    # agree-control gate failed -> rendered as a grey letter, never
    # promoted to a valid-looking verdict
    out = []
    for ch in s:
        if ch.islower():
            out.append(r"\textcolor{gray}{%s}" % ch.upper())
        else:
            out.append(ch)
    return r"\texttt{%s}" % "".join(out)


def phase_table():
    t = j("phase_table_rvp31.json")["table"]
    rows = []
    for f in PHASE_FAMILIES:
        cells = " & ".join(phase_letters(t[f"{f}|{c}"]) for c, _ in PHASE_CELLS)
        rows.append(f"{FAM_TEX[f]} & {cells} \\\\")
    head = " & ".join(h for _, h in PHASE_CELLS)
    return "\n".join([
        r"% AUTO-GENERATED by scripts/make_paper_tables.py from",
        r"% runs/phase_table_rvp31.json -- do not edit by hand.",
        r"\begin{tabular}{@{}l cccccc@{}}",
        r"\toprule",
        r" & \multicolumn{3}{c}{TinyStories ($D/N$)}"
        r" & \multicolumn{3}{c}{web ($D/N$)} \\",
        r"\cmidrule(lr){2-4} \cmidrule(lr){5-7}",
        f"family & {head} \\\\",
        r"\midrule",
        *rows,
        r"\bottomrule",
        r"\end{tabular}",
    ])


def ratio_tex(v):
    if v >= 100:
        return f"{v:,.0f}".replace(",", r"{,}")
    return f"{v:.2f}"


def step6_table():
    cells = j("eval_step6.json")["cells"]
    # rule:prior window ratio per corpus. Kill rows: battery girl-name
    # cues (f*-v2 counter). Rescue rows: injected, battery-disjoint cue
    # names (rescue-cue counter, DECISIONS 2026-06-11), starred. Web
    # base: battery girl names are not single tokens in the web vocab,
    # so the frozen counter cannot measure the class (dash).
    ratio_src = {
        "v1_repro": ("support_ts_window.json", ""),
        "kill_p437": ("support_kill_p437_window.json", ""),
        "kill_p645": ("support_kill_p645_window.json", ""),
        "kill_p1000": ("support_kill_p1000_window.json", ""),
        "web_packed_v2": (None, ""),
        "rescue_d001": ("support_rescuecues_rescue_d001_window.json", "*"),
        "rescue_d010": ("support_rescuecues_rescue_d010_window.json", "*"),
        "rescue_d100": ("support_rescuecues_rescue_d100_window.json", "*"),
        "rescue_d300": ("support_rescuecues_rescue_d300_window.json", "*"),
    }
    order = [("v1_repro", "TS base", "---"),
             ("kill_p437", "kill", "0.437"),
             ("kill_p645", "kill", "0.645"),
             ("kill_p1000", "kill", "1.0"),
             ("web_packed_v2", "web base", "---"),
             ("rescue_d001", "rescue", "0.01"),
             ("rescue_d010", "rescue", "0.1"),
             ("rescue_d100", "rescue", "1.0"),
             ("rescue_d300", "rescue", "3.0")]
    valid = j("eval_step6.json")["verdicts"]["kill_cell_intervention_valid"]
    rows = []
    id_map = []
    for cell, arm, dose in order:
        st = cells[cell]["stats"]
        note = ""
        if cell in valid and not valid[cell]:
            note = r"\textsuperscript{\dag}"
        src, star = ratio_src[cell]
        if src is None:
            ratio = "---"
        else:
            r_ = j(src)["classes"]["girl_names"]["support_ratio"]
            ratio = f"{ratio_tex(r_)}{star}"
        id_map.append(f"%   {arm} {dose} = {cell}")
        rows.append(
            f"{arm} & {dose}{note} & {ratio} & "
            f"{st['recovered_valid']}/{st['n']} & {st['died']}/{st['n']} & "
            f"{st['cm_n_valid']} & ${st['cm_final_mean']:+.2f}$ " + r"\\")
    return "\n".join([
        r"% AUTO-GENERATED by scripts/make_paper_tables.py from",
        r"% runs/eval_step6.json and runs/support_*_window.json",
        r"% -- do not edit by hand. Row -> run-cell ID:",
        *id_map,
        r"\begin{tabular}{@{}l r r ccc r@{}}",
        r"\toprule",
        r"arm & dose & rule:prior & rec/3 & died/3 & "
        r"$\cm$-valid & mean $\cm$ \\",
        r"\midrule",
        *rows,
        r"\bottomrule",
        r"\end{tabular}",
    ])


# Verdict classes for the prediction ledger. Every row is one of:
#   PASS / FAIL          scored against its frozen threshold
#   UNSC                 unscoreable under a registered validity gate
#                        (never converted to PASS or FAIL)
PASS, FAIL, UNSC = "pass", "fail", "unsc"
MARK = {PASS: r"\predpass", FAIL: r"\predfail", UNSC: r"\predunsc"}


